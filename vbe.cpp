static void bochs_write(uint16_t index, uint16_t value) {
    asm volatile("outw %0, %1" :: "a"(index), "Nd"((uint16_t)0x01CE));
    asm volatile("outw %0, %1" :: "a"(value), "Nd"((uint16_t)0x01CF));
}

static uint16_t bochs_read(uint16_t index) {
    asm volatile("outw %0, %1" :: "a"(index), "Nd"((uint16_t)0x01CE));
    uint16_t val;
    asm volatile("inw %1, %0" : "=a"(val) : "Nd"((uint16_t)0x01CF));
    return val;
}

void vbe_init_addr(uint32_t addr) {
    framebuffer = (uint32_t*)addr;
    fb_width  = 800;
    fb_height = 600;

    // Switch Bochs VBE to graphics mode
    bochs_write(4, 0x00);   // disable VBE
    bochs_write(1, 800);    // width
    bochs_write(2, 600);    // height
    bochs_write(3, 32);     // bpp
    bochs_write(4, 0x41);   // enable + LFB

    // Clear framebuffer
    for (int i = 0; i < fb_width * fb_height; i++)
        framebuffer[i] = 0;
}

void vbe_init() {
    vbe_init_addr(0xFD000000);
}

void vbe_putpixel(int x, int y, uint32_t color) {
    if (x < 0 || x >= fb_width || y < 0 || y >= fb_height) return;
    framebuffer[y * fb_width + x] = color;
}

uint32_t vbe_getpixel(int x, int y) {
    if (x < 0 || x >= fb_width || y < 0 || y >= fb_height) return 0;
    return framebuffer[y * fb_width + x];
}

void vbe_clear(uint32_t color) {
    for (int i = 0; i < fb_width * fb_height; i++)
        framebuffer[i] = color;
}

void vbe_fill_rect(int x, int y, int w, int h, uint32_t color) {
    for (int dy = 0; dy < h; dy++)
        for (int dx = 0; dx < w; dx++)
            vbe_putpixel(x + dx, y + dy, color);
}

void vbe_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg) {
    if (c < 32 || c > 127) c = '?';
    const uint8_t* glyph = font[c - 32];
    for (int row = 0; row < 8; row++) {
        for (int col = 0; col < 8; col++) {
            if (glyph[row] & (0x80 >> col))
                vbe_putpixel(x + col, y + row, fg);
            else
                vbe_putpixel(x + col, y + row, bg);
        }
    }
}

void vbe_draw_string(int x, int y, const char* str, uint32_t fg, uint32_t bg) {
    int cx = x;
    for (int i = 0; str[i]; i++) {
        vbe_draw_char(cx, y, str[i], fg, bg);
        cx += 8;
    }
}
